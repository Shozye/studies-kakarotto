/**
 *  actType 
 * Enum used only to specify type of
 * action in
 * @see JActionPanel
 */
public enum actType {
    /**
     * Action Type that will allow us to drag or increase figure in size
     */
    PICK,
    /**
     * Action symboling creating circle
     */
    CIRCLE,
    /**
     * Action symboling creating triangle
     */
    TRIANGLE,
    /**
     * Action symboling creating rectangle
     */
    RECTANGLE,
    /**
     * Action symboling deleting figure
     */
    DELETE,
    /**
     * default action
     */
    NONE,
}
